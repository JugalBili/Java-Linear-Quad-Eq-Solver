    private void btnQCFindActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnQCFindActionPerformed
        
        //initializing variables from user input 
        double x = Double.parseDouble(this.txaQCX.getText());
        double a = Double.parseDouble(this.txtA.getText()); 
        double h = Double.parseDouble(this.txtH.getText());
        double k = Double.parseDouble(this.txtK.getText());
        
        //finding y value from x value 
        double y = (double)Math.round((a*Math.pow(x-(h),2)+k)*100)*0.01;
        
        this.lblQCY.setText(String.valueOf(y));
    }//GEN-LAST:event_btnQCFindActionPerformed

