    private void btnLCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLCalculateActionPerformed
        
        //setting linear panel invisible and linear_calc panel visible
        Linear.setVisible(false); 
        LinearCalc.setVisible(true); 
        
        //initializing variables from text field input 
        double m = Double.parseDouble(this.txtM.getText()); 
        double b = Double.parseDouble(this.txtB.getText()); 
        
        //array for x and y coordinates 
        double[]LxCord = {-2,-1,0,1,2};
        double[]LyCord = {-2,-1,0,1,2}; 
        
        //sending information to lineCalc method 
        lineCalc(m,b,LxCord,LyCord); 
                
    }//GEN-LAST:event_btnLCalculateActionPerformed
    
    private void lineCalc(double m, double b, double[]LxCord, double[]LyCord )
    {
        
        //loop to set y cordinates according to equation 
        for(int i = 0; i <5; i++)
        {
            LyCord[i] = (m*LxCord[i])+b; 
        }
        
        //displyaing equation in text area according to if number is negative or not 
        if(b < 0)
        {
            txaLineCalc.setText("Equation: y = ("+m+"*x) -"+Math.abs(b)+" \n");
        }
        else if(b > 0 )
        {
            txaLineCalc.setText("Equation: y = ("+m+"*x) +"+b+" \n");
        }
        
        txaLineCalc.setText(txaLineCalc.getText()+"\nCoordinates (x , y): \n"); 
        
        //loop to display graph coordinates 
        for(int i = 0; i <5; i++)
        {
            txaLineCalc.setText(txaLineCalc.getText()+"("+LxCord[i]+" , "+LyCord[i]+")\n");
        }
        
        txaLineCalc.setText(txaLineCalc.getText()+"\nSlope : "+m+"\n");
        
        txaLineCalc.setText(txaLineCalc.getText()+"\nY-Intercept: ("+LxCord[2]+" , "+LyCord[2]+")\n");
        
        txaLineCalc.setText(txaLineCalc.getText()+"\nX-Intercept: ("+(double)Math.round(((-b)/m)*100)*0.01+" , 0)\n");
    }
    
