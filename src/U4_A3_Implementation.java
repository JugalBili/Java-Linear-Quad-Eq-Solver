    private void btnCalculateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalculateActionPerformed
        
        //setting quadratics panel invisible and quadratics_calc panel visible
        Quadratics.setVisible(false); 
        QuadraticsCalc.setVisible(true); 
        
        //initializing variables from texe field input 
        double a = Double.parseDouble(this.txtA.getText()); 
        double h = Double.parseDouble(this.txtH.getText()); 
        double k = Double.parseDouble(this.txtK.getText()); 
        
        // setting arrays fro x and y coordinates 
        double[]xCord = {-2,-1,0,1,2};
        double[]yCord = {4,1,0,1,4}; 
        
        //sending information to quadcalc method
        quadCalc(a,h,k,xCord,yCord); 
        
    }//GEN-LAST:event_btnCalculateActionPerformed
    
    private void quadCalc(double a, double h,double k, double[]xCord, double[]yCord )
    {
        //loop to set x and y coordinated according to mapping notation 
        for(int i = 0; i <5; i++)
        {
            xCord[i] = xCord[i]-(-h); 
            yCord[i] = (a*(yCord[i]))+k; 
        }
        
        // setting text for equation accounding to if variables are negative or not 
        if(h < 0 && k >0 )
        {
            txaQuadCalc.setText("Equation: y = "+a+"(x+"+Math.abs(h)+")^2+"+k+"\n");
        }
        else if(k < 0 && h > 0 )
        {
            txaQuadCalc.setText("Equation: y = "+a+"(x-"+h+")^2-"+Math.abs(k)+"\n");
        }
        else if(h<0 && k<0 )
        {
            txaQuadCalc.setText("Equation: y = "+a+"(x+"+Math.abs(h)+")^2-"+Math.abs(k)+"\n");
        }
        
        
        txaQuadCalc.setText(txaQuadCalc.getText()+"\nCoordinates (x , y): \n"); 
        
        //displaying coordinates for the graph 
        for(int i = 0; i <5; i++)
        {
            txaQuadCalc.setText(txaQuadCalc.getText()+"("+xCord[i]+" , "+yCord[i]+")\n");
        }
        
        txaQuadCalc.setText(txaQuadCalc.getText()+"\nVertex: ("+xCord[2]+" , "+yCord[2]+")\n");
        
        //calculating y intercept 
        double yIntercept = (((0-(h))*(0-(h)))*a)+(k); 
        txaQuadCalc.setText(txaQuadCalc.getText()+"\nY-Intercept: (0 , "+yIntercept+")");
    }
    
